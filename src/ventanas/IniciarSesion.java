    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String usern = this.username.getText().toLowerCase();
        String prior = this.prioridad.getText().toLowerCase();
        
        boolean avanzar = true;
        if(prior.equals("a")){
            prior = "prioridad_alta";

        }else if (prior.equals("m")){
            prior = "prioridad_media";
 
        }else if (prior.equals("b")){
            prior = "prioridad_baja";}
        else{
            JOptionPane.showMessageDialog(rootPane, "INTRODUZCA UNA PRIORIDAD V√ÅLIDA");
            avanzar = false;
        }
        if(avanzar == true){
            if(tablaDisp.buscarUsuario(usern) != null && !tablaDisp.buscarUsuario(usern).getNombreUsuario().equals("")){
                JOptionPane.showMessageDialog(rootPane, "El usuario ya se encuentra registrado", "Error", HEIGHT);
                this.username.setText("");
                this.prioridad.setText("");
            }else{
                this.usuario = new NodoUsuario(usern, prior);
                tablaDisp.insertar_NodoUsuario(this.usuario);
                JOptionPane.showMessageDialog(rootPane, "BIENVENIDO!\nUsuario Registrado Exitosamente", "Crear Usuario", HEIGHT);
                Menu m = new Menu(monticulo,tablaDisp,time,usuario);
                this.dispose();
            }}
            
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
        String us = this.iniciar_sesion.getText();
        NodoUsuario usuario = tablaDisp.buscarUsuario(us);
        if(usuario != null){
            JOptionPane.showMessageDialog(rootPane, "BIENVENIDO", us, HEIGHT);
            this.usuario = usuario;
            Menu m = new Menu(monticulo,tablaDisp,time, usuario);
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(rootPane, "USUARIO NO REGISTRADO. CREE UN USUARIO PARA CONTINUAR", us, HEIGHT);
            this.iniciar_sesion.setText("");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

